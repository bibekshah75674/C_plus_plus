#include <iostream>
using namespace std;

int main(){
    int a = 4,b = 5;
    //Arithmetic operators
    cout<<"The value of a + b is "<<a+b<<endl;
    cout<<"The value of a - b is "<<a-b<<endl;
    cout<<"The value of a * b is "<<a*b<<endl;
    cout<<"The value of a / b is "<<a/b<<endl;
    cout<<"The value of a % b is "<<a%b<<endl;

    cout<<"The value of a++ b is "<<a++<<endl;
    cout<<"The value of a-- b is "<<a--<<endl;
    cout<<"The value of ++a b is "<<++a<<endl;
    cout<<"The value of --a b is "<<--a;
    return 0;
}